public class LNode{

    private int value;
    private LNode next;

    public LNode getNext(){
	return next;
    }
    public int getValue(){
	return value;
    }
    public void setNext(int n){
	next = n;
    }
    public void setValue(int n){
	value = n;
    }
    public String toString(){
	return "" + getValue();
    }

}
